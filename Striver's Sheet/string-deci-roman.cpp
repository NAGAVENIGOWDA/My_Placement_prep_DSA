class Solution{
  public:
    string convertToRoman(int n) {
        // code here
        
        vector<pair<int,string>> valueSymb=
        {
            {1000, "M"}, {900, "CM"}, {500, "D"}, {400, "CD"},
            {100, "C"}, {90, "XC"}, {50, "L"}, {40, "XL"},
            {10, "X"}, {9, "IX"}, {5, "V"}, {4, "IV"}, {1, "I"}
        };
        
    string res="";
    
    for(const auto &it:valueSymb){
        int value=it.first;
        string sym=it.second;
        
        while(n>=value){
            res+=sym;
            n-=value;
        }
    }
    
    return res;
    }
};

